package ma.fpl.tp2ex11.entity;

public class nombre {
    private double n2;
    private double n1;

    public nombre(double n2, double n1) {
        this.n2 = n2;
        this.n1 = n1;
    }

    public double getN2() {
        return n2;
    }

    public void setN2(double n2) {
        this.n2 = n2;
    }

    public double getN1() {
        return n1;
    }

    public void setN1(double n1) {
        this.n1 = n1;
    }
}
